package ui

import (
	"fmt"
	"strings"

	"github.com/fatih/color"
	"github.com/sankettank66/localmind/ollama"
)

var (
	header   = color.New(color.FgCyan, color.Bold)
	success  = color.New(color.FgGreen, color.Bold)
	metric   = color.New(color.FgYellow)
	errColor = color.New(color.FgRed)
	Info     = color.New(color.FgBlue)
)

func ShowLoading(message string) {
	fmt.Printf("  %s %-30s", Info.Sprint("‚Ćõ"), message)
}

func UpdateLoading(success bool, message string) {
	fmt.Print("\r")
	if success {
		fmt.Printf("  %s %-30s\n", color.GreenString("‚úÖ"), message)
	} else {
		fmt.Printf("  %s %-30s\n", color.RedString("‚ĚĆ"), message)
	}
}

func PrintResult(r ollama.ModelResult) {
	fmt.Println(strings.Repeat("‚ĒÄ", 60))
	header.Printf("  MODEL: %s\n", r.Model)
	fmt.Println(strings.Repeat("‚ĒÄ", 60))

	if r.Error != nil {
		errColor.Printf("  ERROR: %s\n\n", r.Error)
		return
	}

	fmt.Printf("  %s\n\n", r.Response)
	metric.Printf("  ‚ŹĪ  TTFT: %s\n", r.TTFT.Round(1000000))
	metric.Printf("  ‚ŹĪ  Total: %s\n\n", r.TotalTime.Round(1000000))
}

